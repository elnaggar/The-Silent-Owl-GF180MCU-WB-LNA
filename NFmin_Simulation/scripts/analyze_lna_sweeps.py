def plot_sopt(
    df: pd.DataFrame,
    out_dir: Path,
    title_label: str,
    annotation_metric: str = "NFmin_dB",
) -> None:
    plt.figure(figsize=(7, 7))
    theta = np.linspace(0, 2 * np.pi, 400)
    plt.plot(np.cos(theta), np.sin(theta), "k--", linewidth=1.2, alpha=0.7)
    for (device, topology), sub in df.groupby(["device", "topology"]):
        style = style_for(device, topology)
        plt.scatter(
            sub["SOpt_real"],
            sub["SOpt_imag"],
            s=22,
            alpha=0.85,
            edgecolors="none",
            color=style["color"],
            marker=style["marker"],
            label=f"{device} {topology}",
        )
    if annotation_metric in df.columns:
        best_idx = df.groupby(["device", "topology"], dropna=False)[annotation_metric].idxmin()
        best_points = df.loc[best_idx].copy()
        for _, row in best_points.iterrows():
            style = style_for(row["device"], row["topology"])
            label = (
                f"{row['device'].replace('nfet_', '')} {row['topology']}\n"
                f"({row['SOpt_real']:.3f}, {row['SOpt_imag']:.3f})\n"
                f"|G|={row['SOpt_mag']:.3f}"
            )
            plt.scatter(
                [row["SOpt_real"]],
                [row["SOpt_imag"]],
                s=80,
                color=style["color"],
                marker=style["marker"],
                edgecolors="black",
                linewidths=0.7,
                zorder=5,
            )
            plt.annotate(
                label,
                (row["SOpt_real"], row["SOpt_imag"]),
                xytext=(6, 6),
                textcoords="offset points",
                fontsize=7,
                bbox={"boxstyle": "round,pad=0.25", "fc": "white", "ec": "0.7", "alpha": 0.85},
            )
    plt.xlabel("Re(SOpt)")
    plt.ylabel("Im(SOpt)")
    plt.title(f"{title_label}: Optimum Source Reflection Coefficient")
    plt.axis("equal")
    plt.grid(True, alpha=0.35)
    plt.legend(fontsize=8, ncol=2)
    save_fig(out_dir / "06_sopt_plane.png")


def plot_vgs_curves(df: pd.DataFrame, out_dir: Path, title_label: str) -> None:
    for (device, topology), sub in df.groupby(["device", "topology"]):
        style = style_for(device, topology)
        best_geom = (
            sub.groupby(["W_m", "L_m"])["NFmin_dB"]
            .min()
            .sort_values()
            .head(4)
            .index
        )
        plt.figure(figsize=(10, 6))
        geom_colors = plt.cm.tab10(np.linspace(0, 1, max(len(best_geom), 1)))
        line_styles = ["-", "--", "-.", ":"]
        for idx, (w_m, l_m) in enumerate(best_geom):
            curve = sub[(sub["W_m"].eq(w_m)) & (sub["L_m"].eq(l_m))]
            curve = curve.sort_values("VBIAS_set_V")
            label = f"W={w_m * 1e6:g}um L={l_m * 1e6:g}um"
            plt.plot(
                curve["VBIAS_set_V"],
                curve["NFmin_dB"],
                linewidth=2.0,
                color=geom_colors[idx],
                marker=style["marker"],
                markersize=3,
                markevery=max(len(curve) // 18, 1),
                alpha=0.95,
                linestyle=line_styles[idx % len(line_styles)],
                label=label,
            )
        plt.xlabel("Bias voltage / swept VGS target (V)")
        plt.ylabel("NFmin (dB)")
        plt.title(f"{title_label}: {device} {topology} Best Geometry NFmin Curves")
        plt.grid(True, alpha=0.35)
        plt.legend(fontsize=8)
        safe = f"{device}_{topology}".replace("/", "_").replace(" ", "_")
        save_fig(out_dir / f"07_nfmin_vs_bias_{safe}.png")


def generate_plot_set(
    df: pd.DataFrame,
    out_dir: Path,
    title_label: str,
    group_file: list[str],
    annotation_metric: str,
) -> None:
    out_dir.mkdir(parents=True, exist_ok=True)
    best_by_file = best_rows(df, group_file, "NFmin_dB")
    plot_nfmin_vs_jd(df, out_dir, title_label)
    plot_best_nfmin_bar(best_by_file, out_dir, title_label)
    plot_tradeoff(best_by_file, out_dir, title_label)
    plot_gmid_vs_jd(df, out_dir, title_label)
    plot_gmro_vs_jd(df, out_dir, title_label)
    plot_sopt(df, out_dir, title_label, annotation_metric)
    plot_vgs_curves(df, out_dir, title_label)


def format_value(value: object) -> str:
    if isinstance(value, (float, np.floating)):
        if not np.isfinite(value):
            return "nan"
        return f"{value:.6g}"
    return str(value)


def format_table(df: pd.DataFrame, columns: list[str]) -> str:
    if df.empty:
        return "(no rows)"
    table = df[columns].copy()
    formatted = table.applymap(format_value)
    widths = {
        col: max(len(col), formatted[col].astype(str).map(len).max())
        for col in formatted.columns
    }

    header = "  ".join(col.ljust(widths[col]) for col in formatted.columns)
    rule = "  ".join("-" * widths[col] for col in formatted.columns)
    rows = [
        "  ".join(str(row[col]).ljust(widths[col]) for col in formatted.columns)
        for _, row in formatted.iterrows()
    ]
    return "\n".join([header, rule, *rows])


