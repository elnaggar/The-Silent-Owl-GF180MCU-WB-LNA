def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "files",
        nargs="*",
        help="Sweep text files. If omitted, the default eight files are used.",
    )
    parser.add_argument(
        "-o",
        "--out-dir",
        default=str(DEFAULT_RESULTS_DIR / "lna_sweep_analysis"),
        help="Output directory for plots and text summaries.",
    )
    parser.add_argument(
        "--min-gm-s",
        type=float,
        default=DEFAULT_FILTERS["min_gm_s"],
        help="Minimum gm in siemens for an LNA-usable point.",
    )
    parser.add_argument(
        "--min-id-a",
        type=float,
        default=DEFAULT_FILTERS["min_id_a"],
        help="Minimum drain current in amperes for an LNA-usable point.",
    )
    parser.add_argument(
        "--min-pdc-mw",
        type=float,
        default=DEFAULT_FILTERS["min_pdc_mw"],
        help="Minimum DC power in mW for an LNA-usable point.",
    )
    parser.add_argument(
        "--max-inv-gm-ohm",
        type=float,
        default=DEFAULT_FILTERS["max_inv_gm_ohm"],
        help="Maximum 1/gm or CG Rin estimate allowed in ohms.",
    )
    parser.add_argument(
        "--cg-min-rin-ohm",
        type=float,
        default=DEFAULT_FILTERS["cg_min_rin_ohm"],
        help="Minimum allowed common-gate Rin estimate in ohms.",
    )
    parser.add_argument(
        "--cg-max-rin-ohm",
        type=float,
        default=DEFAULT_FILTERS["cg_max_rin_ohm"],
        help="Maximum allowed common-gate Rin estimate in ohms.",
    )
    args = parser.parse_args()

    paths = [Path(p) for p in args.files] if args.files else discover_default_files()
    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    frames = []
    for path in paths:
        print(f"Reading {path}")
        frames.append(read_sweep(path))

    df = pd.concat(frames, ignore_index=True)
    raw_df = add_scores(df)

    filters = {
        "min_gm_s": args.min_gm_s,
        "min_id_a": args.min_id_a,
        "min_pdc_mw": args.min_pdc_mw,
        "max_inv_gm_ohm": args.max_inv_gm_ohm,
        "cg_min_rin_ohm": args.cg_min_rin_ohm,
        "cg_max_rin_ohm": args.cg_max_rin_ohm,
    }
    usable_df, rejected_df = apply_lna_filter(df, **filters)
    if usable_df.empty:
        raise SystemExit(
            "All points were rejected by the practical LNA filter.\n"
            "Try relaxing thresholds, for example:\n"
            "  python3 analyze_lna_sweeps.py --min-gm-s 1e-4 --max-inv-gm-ohm 10000\n"
        )
    usable_df = add_scores(usable_df)

    group_file = ["device", "topology", "source_file"]
    group_geom = ["device", "topology", "W_m", "L_m", "VDS_set_V"]

    best_by_file = best_rows(usable_df, group_file, "NFmin_dB")
    best_by_geom = best_rows(usable_df, group_geom, "NFmin_dB")
    best_by_score = best_rows(usable_df, group_geom, "score")

    generate_plot_set(
        raw_df,
        out_dir / "plots_all_data",
        "All Raw Sweep Points",
        group_file,
        "NFmin_dB",
    )
    generate_plot_set(
        usable_df,
        out_dir / "plots_filtered_usable",
        "Filtered Usable LNA Points",
        group_file,
        "design_score",
    )
    write_text_reports(
        usable_df,
        rejected_df,
        best_by_file,
        best_by_geom,
        best_by_score,
        out_dir,
        filters,
    )

    print()
    print(f"Done. Results written to: {out_dir.resolve()}")
    print(f"Usable points: {len(usable_df)}")
    print(f"Rejected points: {len(rejected_df)}")
    print("Plots written to plots_all_data/ and plots_filtered_usable/.")
    print("Open summary_best_overall.txt first, then summary_filter_report.txt.")


if __name__ == "__main__":
    main()
