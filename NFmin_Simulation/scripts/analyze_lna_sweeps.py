def parse_name(path: Path) -> tuple[str, str]:
    match = re.search(r"nmos(\d+)_(cg|cs)", path.name.lower())
    if not match:
        return path.stem, "unknown"
    device_code, topology = match.groups()
    return DEVICE_LABELS.get(device_code, f"nmos{device_code}"), topology.upper()


def read_sweep(path: Path) -> pd.DataFrame:
    if not path.exists():
        raise FileNotFoundError(path)

    df = pd.read_csv(path, sep=r"\s+", comment="*", engine="python")
    device, topology = parse_name(path)
    df["device"] = device
    df["topology"] = topology
    df["source_file"] = path.name

    if "Rin_CG_est_Ohm" in df.columns:
        df["Rin_or_inv_gm_Ohm"] = df["Rin_CG_est_Ohm"]
    elif "inv_gm_Ohm" in df.columns:
        df["Rin_or_inv_gm_Ohm"] = df["inv_gm_Ohm"]
    else:
        df["Rin_or_inv_gm_Ohm"] = np.nan

    for col in df.columns:
        if col not in {"device", "topology", "source_file"}:
            df[col] = pd.to_numeric(df[col], errors="coerce")

    df = df.replace([np.inf, -np.inf], np.nan)
    df = df.dropna(subset=["NFmin_dB", "JD_mA_per_um", "gm_ID_1_per_V", "gmro"])
    df = df[df["ID_A"] > 0]
    return df


def discover_default_files() -> list[Path]:
    for directory in DEFAULT_SEARCH_DIRS:
        paths = [directory / name for name in DEFAULT_FILENAMES]
        if all(path.exists() for path in paths):
            return paths

    missing_report = []
    for directory in DEFAULT_SEARCH_DIRS:
        missing = [name for name in DEFAULT_FILENAMES if not (directory / name).exists()]
        missing_report.append(f"{directory}: missing {len(missing)} files")
    raise SystemExit(
        "Could not find the eight default sweep files.\n"
        "Either run the script in the folder containing the .txt files, or pass the files manually.\n\n"
        + "\n".join(missing_report)
    )


def minmax(series: pd.Series) -> pd.Series:
    lo = series.min(skipna=True)
    hi = series.max(skipna=True)
    if not np.isfinite(lo) or not np.isfinite(hi) or math.isclose(lo, hi):
        return pd.Series(0.0, index=series.index)
    return (series - lo) / (hi - lo)


def add_scores(df: pd.DataFrame) -> pd.DataFrame:
    df = df.copy()
    if "SOpt_mag" not in df.columns:
        df["SOpt_mag"] = np.sqrt(df["SOpt_real"] ** 2 + df["SOpt_imag"] ** 2)
    df["cg_50ohm_error"] = np.nan
    is_cg = df["topology"].eq("CG")
    df.loc[is_cg, "cg_50ohm_error"] = (
        (df.loc[is_cg, "Rin_or_inv_gm_Ohm"] - 50.0).abs() / 50.0
    )
    df["cg_50ohm_error"] = df["cg_50ohm_error"].fillna(0.0).clip(upper=5.0)
    df["sopt_edge_error"] = df["SOpt_mag"].clip(lower=0.0, upper=1.5)

    df["score"] = (
        0.45 * minmax(df["NFmin_dB"])
        + 0.20 * minmax(df["Pdc_mW"])
        + 0.15 * minmax(df["Rn_Ohm"])
        + 0.10 * minmax(df["cg_50ohm_error"])
        + 0.10 * minmax(-df["gm_ID_1_per_V"])
    )
    df["design_score"] = (
        0.35 * minmax(df["NF_dB"])
        + 0.25 * minmax(df["Pdc_mW"])
        + 0.15 * minmax(df["sopt_edge_error"])
        + 0.10 * minmax(df["Rn_Ohm"])
        + 0.10 * minmax(df["cg_50ohm_error"])
        + 0.05 * minmax(-df["gm_S"])
    )
    return df


def apply_lna_filter(
    df: pd.DataFrame,
    min_gm_s: float,
    min_id_a: float,
    min_pdc_mw: float,
    max_inv_gm_ohm: float,
    cg_min_rin_ohm: float,
    cg_max_rin_ohm: float,
) -> tuple[pd.DataFrame, pd.DataFrame]:
    df = df.copy()
    df["filter_reason"] = "usable"

    near_off = (
        (df["gm_S"] < min_gm_s)
        | (df["ID_A"] < min_id_a)
        | (df["Pdc_mW"] < min_pdc_mw)
        | (df["Rin_or_inv_gm_Ohm"] > max_inv_gm_ohm)
    )
    df.loc[near_off, "filter_reason"] = "near_off_or_too_low_gm"

    is_cg = df["topology"].eq("CG")
    bad_cg_match = is_cg & (
        (df["Rin_or_inv_gm_Ohm"] < cg_min_rin_ohm)
        | (df["Rin_or_inv_gm_Ohm"] > cg_max_rin_ohm)
    )
    df.loc[bad_cg_match & ~near_off, "filter_reason"] = "cg_rin_not_near_50ohm"

    usable = df[df["filter_reason"].eq("usable")].copy()
    rejected = df[~df["filter_reason"].eq("usable")].copy()
    return usable, rejected


def style_for(device: str, topology: str) -> dict[str, object]:
    return STYLE_MAP.get((device, topology), {"color": "#333333", "marker": "o"})


def best_rows(df: pd.DataFrame, group_cols: list[str], metric: str) -> pd.DataFrame:
    if df.empty:
        return df.copy()
    idx = df.groupby(group_cols, dropna=False)[metric].idxmin()
    cols = [
        "device",
        "topology",
        "VDS_set_V",
        "W_m",
        "L_m",
        "VBIAS_set_V",
        "VGS_actual_V",
        "VDS_actual_V",
        "VBS_actual_V",
        "ID_A",
        "JD_mA_per_um",
        "Pdc_mW",
        "gm_S",
        "gds_S",
        "gm_ID_1_per_V",
        "ro_Ohm",
        "gmro",
        "Rin_or_inv_gm_Ohm",
        "NFmin_dB",
        "NF_dB",
        "Rn_Ohm",
        "SOpt_real",
        "SOpt_imag",
        "SOpt_mag",
        "cg_50ohm_error",
        "score",
        "design_score",
        "source_file",
    ]
    return df.loc[idx, [c for c in cols if c in df.columns]].sort_values(metric)


def save_fig(path: Path) -> None:
    plt.tight_layout()
    plt.savefig(path, dpi=180)
    plt.close()


def annotate_bars(ax, values: pd.Series, fmt: str = "{:.3g}") -> None:
    y_max = max(float(values.max()), 1e-12)
    ax.set_ylim(top=y_max * 1.18)
    for patch, value in zip(ax.patches, values):
        ax.annotate(
            fmt.format(value),
            (patch.get_x() + patch.get_width() / 2, patch.get_height()),
            ha="center",
            va="bottom",
            fontsize=8,
            rotation=0,
            xytext=(0, 3),
            textcoords="offset points",
        )


def plot_nfmin_vs_jd(df: pd.DataFrame, out_dir: Path, title_label: str) -> None:
    plt.figure(figsize=(10, 6))
    for (device, topology), sub in df.groupby(["device", "topology"]):
        label = f"{device} {topology}"
        style = style_for(device, topology)
        plt.scatter(
            sub["JD_mA_per_um"],
            sub["NFmin_dB"],
            s=24,
            alpha=0.85,
            edgecolors="none",
            color=style["color"],
            marker=style["marker"],
            label=label,
        )
    plt.xscale("log")
    plt.xlabel("JD (mA/um)")
    plt.ylabel("NFmin (dB)")
    plt.title(f"{title_label}: NFmin vs Current Density")
    plt.grid(True, which="both", alpha=0.35)
    plt.legend(fontsize=8, ncol=2)
    save_fig(out_dir / "01_nfmin_vs_jd_all.png")


def plot_best_nfmin_bar(best: pd.DataFrame, out_dir: Path, title_label: str) -> None:
    if best.empty:
        return
    data = best.copy()
    data["label"] = data["device"] + " " + data["topology"]
    colors = [style_for(row.device, row.topology)["color"] for row in data.itertuples()]
    fig, ax = plt.subplots(figsize=(10, 5))
    ax.bar(data["label"], data["NFmin_dB"], color=colors)
    annotate_bars(ax, data["NFmin_dB"], "{:.4g}")
    ax.set_ylabel("Best NFmin (dB)")
    ax.set_title(f"{title_label}: Best NFmin per Device and Topology")
    ax.tick_params(axis="x", rotation=35)
    for tick in ax.get_xticklabels():
        tick.set_ha("right")
    ax.grid(axis="y", alpha=0.35)
    save_fig(out_dir / "02_best_nfmin_bar.png")


